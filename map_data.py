# 맵 로드 함수
def load_map(map_name='main'):
    """
    맵 이름으로 맵 데이터 반환

    Args:
        map_name (str): 'main' (기본값)

    Returns:
        list: 2차원 배열 맵 데이터
    """
    maps = {
        'main': MAIN_MAP,
    }

    if map_name in maps:
        return maps[map_name]
    else:
        print(f"맵 '{map_name}'을(를) 찾을 수 없습니다. 기본 맵(main)을 반환합니다.")
        return MAIN_MAP

# 맵 정보 출력
def print_map_info(map_data):
    """맵 정보 출력"""
    height = len(map_data)
    width = len(map_data[0]) if height > 0 else 0
    print(f"맵 크기: {width} x {height} 타일")
    print(f"실제 크기: {width * 64} x {height * 64} 픽셀")

    # 타일 통계
    tile_counts = {}
    for row in map_data:
        for tile in row:
            tile_counts[tile] = tile_counts.get(tile, 0) + 1

    print("타일 사용 현황:")
    for tile_type, count in sorted(tile_counts.items()):
        print(f"  타일 {tile_type}: {count}개")
